    public void tampil_data() {
    DefaultTableModel tabel = new DefaultTableModel();
    tabel.addColumn("id_number");
    tabel.addColumn("no_rumah");
    tabel.addColumn("nama");
    tabel.addColumn("nomor_hp");
    tabel.addColumn("alamat");

    try {
        java.sql.Connection conn = com.mycompany.damdes.DAMDES.connect();
        String sql = "SELECT * FROM `subberanda_data`WHERE 1";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            tabel.addRow(new Object[]{
                rs.getString("id_number"),
                rs.getString("no_rumah"),
                rs.getString("nama"),
                rs.getString("nomor_hp"),
                rs.getString("alamat"),
            });
        }
        tabelpelanggan.setModel(tabel);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage());
    }
} 

