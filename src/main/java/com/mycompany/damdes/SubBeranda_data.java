    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
//    this.dispose();

    String id_number = txt_idnum2.getText();
    String no_rumah = txt_nmrrmh2.getText();
    String nama = txt_nama.getText();
    String nomor_hp = txt_nohp.getText();
    String alamat = txt_alamat.getText();

    // Periksa apakah ada input yang kosong
    if (id_number.isEmpty() || no_rumah.isEmpty() || nama.isEmpty() || nomor_hp.isEmpty() || alamat.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Lengkapi Data");
        return;
    }
    
    // Buat query SQL dengan parameter
    String sqlquery = "INSERT INTO subberanda_data (id_number, no_rumah, nama, nomor_hp, alamat) VALUES (?, ?, ?, ?, ?)";

    try (java.sql.Connection conn = com.mycompany.damdes.DAMDES.connect();
         java.sql.PreparedStatement pst = conn.prepareStatement(sqlquery)) {
        
        // Set parameter dalam PreparedStatement
        pst.setString(1, id_number);
        pst.setString(2, no_rumah);
        pst.setString(3, nama);
        pst.setString(4, nomor_hp);
        pst.setString(5, alamat);

        // Eksekusi query
        pst.executeUpdate();

        // Reset teks input setelah berhasil
        txt_idnum2.setText("");
        txt_nmrrmh2.setText("");
        txt_nama.setText("");
        txt_nohp.setText("");
        txt_alamat.setText("");

        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(null, "Data Anda Sukses");
        tampil_data();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Koneksi ke database gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_simpanActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
         try {
        // Mendapatkan koneksi ke database
        java.sql.Connection conn = com.mycompany.damdes.DAMDES.connect();
        
        // Query SQL untuk menghapus data dengan parameter
        String sql = "DELETE FROM subberanda_data WHERE id_number = ?";
        
        // Menggunakan PreparedStatement untuk mengisi parameter
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, txt_idnum2.getText());
        
        // Menjalankan query
        int rowsAffected = pst.executeUpdate();
        
        // Memeriksa apakah ada baris yang terpengaruh (dihapus)
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            
            // Memanggil method tampil_data untuk merefresh tabel
            tampil_data();
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    } catch (SQLException e) {
        // Menampilkan pesan kesalahan
        JOptionPane.showMessageDialog(null, "Proses Penghapusan Gagal: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton5ActionPerformed

