    public void tampil_data() {
    DefaultTableModel tabel = new DefaultTableModel();
    tabel.addColumn("ID Number");
    tabel.addColumn("No Rumah");
    tabel.addColumn("Kode Bayar");
    tabel.addColumn("Bulan");
    tabel.addColumn("Tanggal");
    tabel.addColumn("Jenis Tagihan");

    try {
        java.sql.Connection conn = com.mycompany.damdes.DAMDES.connect();
        String sql = "SELECT * FROM subberanda_tagihan";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            tabel.addRow(new Object[]{
                rs.getString("id_number"),
                rs.getString("no_rumah"),
                rs.getString("kode_bayar"),
                rs.getString("bulan"),
                rs.getString("tanggal"),
                rs.getString("jenis_tagihan")
            });
        }
        jTable1.setModel(tabel);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage());
    }
}
