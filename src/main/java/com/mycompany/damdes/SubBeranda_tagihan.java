    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
    String id_number = txt_idnum.getText();
    String no_rumah = txt_norumah1.getText();
    String kode_bayar = txt_kodebyr.getText();
    String bulan = txt_bulan.getText();
    String tanggal = txt_tanggal.getText();
    String jenis_tagihan = txt_tagihan.getText();

    // Periksa apakah ada input yang kosong
    if (id_number.isEmpty() || no_rumah.isEmpty() || kode_bayar.isEmpty() || bulan.isEmpty() || tanggal.isEmpty() || jenis_tagihan.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Masukkan semua data tagihan.");
        return;
    }

    // Buat query SQL untuk memperbarui data dengan parameter
    String sqlquery = "UPDATE subberanda_tagihan SET no_rumah = ?, kode_bayar = ?, bulan = ?, tanggal = ?, jenis_tagihan = ? WHERE id_number = ?";

    try {
        // Mendapatkan koneksi ke database
        java.sql.Connection conn = com.mycompany.damdes.DAMDES.connect();

        // Menggunakan PreparedStatement untuk membuat query dengan parameter
        java.sql.PreparedStatement pst = conn.prepareStatement(sqlquery);
        
        // Set parameter PreparedStatement
        pst.setString(1, no_rumah);
        pst.setString(2, kode_bayar);
        pst.setString(3, bulan);
        pst.setString(4, tanggal);
        pst.setString(5, jenis_tagihan);
        pst.setString(6, id_number);

        // Eksekusi query
        pst.executeUpdate();

        // Reset teks input setelah berhasil
        txt_idnum.setText("");
        txt_norumah1.setText("");
        txt_kodebyr.setText("");
        txt_bulan.setText("");
        txt_tanggal.setText("");
        txt_tagihan.setText("");

        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(null, "Data Anda berhasil diperbarui.");
        
        // Refresh tabel untuk menampilkan data yang baru diperbarui
        tampil_data();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Koneksi ke database gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
    try {
        // Mendapatkan koneksi ke database
        java.sql.Connection conn = com.mycompany.damdes.DAMDES.connect();
        
        // Query SQL untuk menghapus data dengan parameter
        String sql = "DELETE FROM subberanda_tagihan WHERE id_number = ?";
        
        // Menggunakan PreparedStatement untuk mengisi parameter
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, txt_idnum.getText());
        
        // Menjalankan query
        int rowsAffected = pst.executeUpdate();
        
        // Memeriksa apakah ada baris yang terpengaruh (dihapus)
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            
            // Memanggil method tampil_data untuk merefresh tabel
            tampil_data();
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    } catch (SQLException e) {
        // Menampilkan pesan kesalahan
        JOptionPane.showMessageDialog(null, "Proses Penghapusan Gagal: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btn_hapusActionPerformed

